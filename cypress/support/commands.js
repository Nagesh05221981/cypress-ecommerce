Cypress.Commands.add('seedUser', (user) => {
    const users = JSON.parse(localStorage.getItem('nova_users')) || {}
    users[user.email] = { name: user.name, password: user.password }
    localStorage.setItem('nova_users', JSON.stringify(users))
})